function triangles(count) {
  var num = "#"
 for (var i = 0; i < count; i++) { 
   console.log(num)
    num += "#"
 }
}

////////////////////////////////////////////////////////////////////////////////
// fizzBuzz ////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function fizzBuzz() {
  for (var i = 1; i <= 15; i++ ) {
    if (i % 15 == 0)
        console.log("fizzbuzz");
    else if (i % 3 == 0)
        console.log("fizz");
    else if (i % 5 == 0)
        console.log("buzz");
    else
        console.log(i);
  }
}

