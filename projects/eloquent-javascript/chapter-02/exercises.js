function drawChessboard(input) {
  var output = "";
for(var i = 0; i < input; i++){
 for(var j = 0; j < input; j++){
  output += (i + j) %  2 === 0 ? " " : "#";
 }
 output += "\n";
}
return console.log(output)
}

